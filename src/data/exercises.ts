/** 表示対象の種目（hidden フラグが付いた将来実装予定の種目は除外） */
export const EXERCISES: Exercise[] = ALL_EXERCISES.filter(e => !e.hidden)

export function getExercisesByBodyPart(bodyPart: BodyPart): Exercise[] {
  return EXERCISES.filter(e => e.bodyPart === bodyPart)
}

/** 過去レコードの名前解決のため、hidden な種目も検索対象に含める */
export function getExerciseById(id: string): Exercise | undefined {
  return ALL_EXERCISES.find(e => e.id === id)
}
